def generate_dataset(n=300, seed=42):
    np.random.seed(seed)
    random.seed(seed)

    users = []
    for i in range(1, 31):
        users.append({
            "user_id": i,
            "user_name": f"{random.choice(LAST_NAMES)} {random.choice(FIRST_NAMES)}",
            "phone": f"+7{np.random.randint(900, 999)}{np.random.randint(1000000, 9999999)}",
        })

    data = []
    for i in range(n):
        user = random.choice(users)
        rating = int(np.random.choice([1, 2, 3, 4, 5], p=[0.10, 0.10, 0.15, 0.30, 0.35]))

        if rating >= 4:
            text = random.choice(POSITIVE_REVIEWS)
            sentiment = 1
        elif rating <= 2:
            text = random.choice(NEGATIVE_REVIEWS)
            sentiment = 0
        else:
            text = random.choice(NEUTRAL_REVIEWS)
            sentiment = None

        days_ago = int(np.random.randint(0, 60))
        data.append({
            "id": i,
            "text": text,
            "rating": rating,
            "sentiment": sentiment,
            "date": (datetime.now() - timedelta(days=days_ago)).strftime("%Y-%m-%d"),
            "product": random.choice(PRODUCTS),
            "user_id": user["user_id"],
            "user_name": user["user_name"],
            "phone": user["phone"],
        })

    return pd.DataFrame(data)


def save_to_csv(df, path="data/reviews.csv"):
    os.makedirs("data", exist_ok=True)
    df.to_csv(path, index=False, encoding="utf-8-sig")
    print(f"Saved {len(df)} records to {path}")


